    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            
             if(txtUser.getText().isEmpty() || txtPass.getText().isEmpty() || txtUtype.equals("Select") )
             {
                 JOptionPane.showMessageDialog(this,"UserName or Password Blank");
                 
                }
             else
             {
                  String username = txtUser.getText();
              String password = txtPass.getText(); 
              String utype = txtUtype.getSelectedItem().toString(); 
              
                Class.forName("com.mysql.jdbc.Driver");
             con = DriverManager.getConnection("jdbc:mysql://localhost/studentmanagement","root","");
                 pst = con.prepareStatement("select * from user where username = ? and password = ?");
                 pst.setString(1, username);
               pst.setString(2, password);
               rs = pst.executeQuery();
               
               if(rs.next())
               {  
                   String s1 = rs.getString("utype");
                   if(utype.equalsIgnoreCase("Student") && s1.equalsIgnoreCase("Student")){
                   
                   
                   StudentPage m = new StudentPage();
                   this.hide();
                   m.setVisible(true);
                   setVisible(false);
                   }
                   if(utype.equalsIgnoreCase("Lecturer") && s1.equalsIgnoreCase("Lecturer")){
                   
                   
                   LecturerPage Lp = new  LecturerPage();
                   this.hide();
                   Lp.setVisible(true);
                   setVisible(false);
                   }
               }
               else
               {
                    JOptionPane.showMessageDialog(this,"UserName or Password do not Match");
                    txtUser.setText("");
                     txtPass.setText("");
                      txtUser.requestFocus();
               }
             
             }
             
             
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (SQLException ex) {
           
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
               
    }//GEN-LAST:event_jButton1ActionPerformed

